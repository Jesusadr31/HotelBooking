    /**
     * Acción realizada al presionar el botón para buscar un cliente.
     * Busca un cliente por nombre y apellido y muestra su información si existe.
     * 
     * @param evt El evento de acción que desencadena esta acción.
     */
    private void btnSearchClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchClientActionPerformed
        String lastName, name;
        
                
        try{
            if(txtFirstNameClient.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Introduzca un nombre y apellido");
            }else{
                String [] input = txtFirstNameClient.getText().split(" ");
            
                name = input[0];
                lastName = input[1];
                if(input.length > 2){
                    JOptionPane.showMessageDialog(null, "No puedes ingresar datos más de los permitidos.");
                }else if(input.length < 2){
                    JOptionPane.showMessageDialog(null, "Error, falta el apellido.");
                }else{
                    txtAreaClient.setText(status.searchClient(name, lastName).printClientStatus());
                }

                
           }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Valores inválidos");
        }
        
        
    }//GEN-LAST:event_btnSearchClientActionPerformed

